def make_timeline(block):
    lines = []
    start = 0
    for idx, frame in enumerate(block["frames"], 1):
        end = start + (2 if idx in (1, 3, 5) else 3)
        desc = frame.split("。", 1)[-1].strip()
        if not desc:
            desc = frame
        lines.append(
            f"[{start}s-{end}s]对应分镜{idx}:{desc}；环境动态和人物动作严格继承草图，保持空间方向、视线方向、光源方向连续。"
        )
        start = end
    return "\n".join(lines)


